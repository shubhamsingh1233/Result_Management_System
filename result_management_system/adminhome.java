    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String course=(String)jComboBox1.getSelectedItem();
        String branch=(String)jComboBox2.getSelectedItem();
        String roll=jTextField1.getText();
        String name=jTextField2.getText();
        String gender=(String)jComboBox3.getSelectedItem();
        String father=jTextField4.getText();
        
        try
        {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srms", "root", "Shubham07@");
            
            Statement st=con.createStatement();
            st.executeUpdate("insert into student(roll, course, branch, name, gender, father) values('"+roll+"', '"+course+"', '"+branch+"', '"+name+"', '"+gender+"', '"+father+"')" );
            
            JOptionPane.showMessageDialog(null, "Data Saved Succesfully");
            
            setVisible(false);
            new adminhome().setVisible(true);
            
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e.toString());
        }
        
        
    }//GEN-LAST:event_jButton6ActionPerformed

